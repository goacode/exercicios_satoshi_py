""""
18. Receba 2 valores inteiros. Calcule e mostre o resultado da diferença do
maior pelo menor valor.
"""

n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))

if n1 > n2:
    print(f"{n1} - {n2} = {n1 - n2}")

else:
    print(f"{n2} - {n1} = {n2 - n1}")
    